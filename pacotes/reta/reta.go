package main

import "math"

// Iniciando com letra maiuscula é PUBLICA (dentro e fora do pacote)
// não pode ter duas funções publicas dentro do mesmo pacote (pasta)

// Ponto representa uma coordenada no plano cartesiano
type Ponto struct {
	x float64
	y float64
}

func catetos(a, b Ponto) (cx, cy float64) {
	cx = math.Abs(b.x - a.x)
	cy = math.Abs(b.y - a.y)
	return
}

// Distancia é esponsavel por calcular a distancia linear entre dois posntos
func Distancia(a, b Ponto) float64 {
	cx, cy := catetos(a, b)
	return math.Sqrt(math.Pow(cx, 2) + math.Pow(cy, 2))
}
